package com.proit.modelo.auxiliar;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.Calendar;

public class ReporteTotalEvento implements Serializable{
	private static final long serialVersionUID = 1L;
	
	private int idEvento;
	private String cliente;
	private String evento;
	private Calendar fecha;
	private String responsableEvento;
	private BigDecimal costoTotal;
	private BigDecimal costoTotalConIVA;
	private BigDecimal pagadoTotal;
	private BigDecimal pendienteTotal;
	private BigDecimal totalEvento;
	private BigDecimal totalEventoConIVA;
	private boolean cumplido;
	private boolean cerrado;
	private boolean costoFinal;
	
	public int getIdEvento() {
		return idEvento;
	}
	public void setIdEvento(int idEvento) {
		this.idEvento = idEvento;
	}
	public String getCliente() {
		return cliente;
	}
	public void setCliente(String cliente) {
		this.cliente = cliente;
	}
	public String getEvento() {
		return evento;
	}
	public void setEvento(String evento) {
		this.evento = evento;
	}
	public Calendar getFecha() {
		return fecha;
	}
	public void setFecha(Calendar fecha) {
		this.fecha = fecha;
	}
	public String getResponsableEvento() {
		return responsableEvento;
	}
	public void setResponsableEvento(String responsableEvento) {
		this.responsableEvento = responsableEvento;
	}
	public BigDecimal getCostoTotal() {
		return costoTotal;
	}
	public void setCostoTotal(BigDecimal costoTotal) {
		this.costoTotal = costoTotal;
	}
	public BigDecimal getCostoTotalConIVA() {
		return costoTotalConIVA;
	}
	public void setCostoTotalConIVA(BigDecimal costoTotalConIVA) {
		this.costoTotalConIVA = costoTotalConIVA;
	}
	public BigDecimal getPagadoTotal() {
		return pagadoTotal;
	}
	public void setPagadoTotal(BigDecimal pagadoTotal) {
		this.pagadoTotal = pagadoTotal;
	}
	public BigDecimal getPendienteTotal() {
		return pendienteTotal;
	}
	public void setPendienteTotal(BigDecimal pendienteTotal) {
		this.pendienteTotal = pendienteTotal;
	}
	public BigDecimal getTotalEvento() {
		return totalEvento;
	}
	public void setTotalEvento(BigDecimal totalEvento) {
		this.totalEvento = totalEvento;
	}
	public BigDecimal getTotalEventoConIVA() {
		return totalEventoConIVA;
	}
	public void setTotalEventoConIVA(BigDecimal totalEventoConIVA) {
		this.totalEventoConIVA = totalEventoConIVA;
	}
	public boolean isCumplido() {
		return cumplido;
	}
	public void setCumplido(boolean cumplido) {
		this.cumplido = cumplido;
	}
	public boolean isCerrado() {
		return cerrado;
	}
	public void setCerrado(boolean cerrado) {
		this.cerrado = cerrado;
	}
	public boolean isCostoFinal() {
		return costoFinal;
	}
	public void setCostoFinal(boolean costoFinal) {
		this.costoFinal = costoFinal;
	}
	
}
